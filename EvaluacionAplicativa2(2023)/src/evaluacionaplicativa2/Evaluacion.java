    private void Igv(){
        double resultado;
        double monto = Double.parseDouble(txtMonto.getText());
        
        resultado = monto * 0.18;
        
        txtIgv.setText(""+resultado);
        
    }
    
    private void Impuesto1(){
        double monto = Double.parseDouble(txtMonto.getText());
        double porcentaje;
        
        if (monto < 200) {
                porcentaje = 12.56;
            } else if (monto == 200) {
                porcentaje = 6.95;
            } else {
                porcentaje = 15;
            }

            double totalImp1 = monto * (porcentaje / 100);
            txtImpuesto1.setText(""+totalImp1);
    }
    
    private void Impuesto2(){
        double monto = Double.parseDouble(txtMonto.getText());
        double porcentaje;
        porcentaje = 20.00;
        
        if (monto >= 100) {
                
                txtImpuesto2.setForeground(Color.BLUE);
            } else {
                txtImpuesto2.setForeground(Color.red);
            } 

            double totalImp2 = monto * (porcentaje / 100);
            txtImpuesto2.setText(""+totalImp2);
    }
    
    private void Impuesto3(){
        double impuesto1 = Double.parseDouble(txtImpuesto1.getText());
        double impuesto2 = Double.parseDouble(txtImpuesto2.getText());
        double sumaImpuestos = impuesto1 + impuesto2;
        double totalImp3;
        
        if (sumaImpuestos <= 20 && sumaImpuestos >= 0) {
                totalImp3 = sumaImpuestos * 0.5 + 10.00;
            } else {
                totalImp3 = sumaImpuestos * 0.25 + 12.00;
            }
        
        txtImpuesto3.setText(""+totalImp3);
    }
    
    private void PromedioImpuestos(){
        double impuesto1 = Double.parseDouble(txtImpuesto1.getText());
        double impuesto2 = Double.parseDouble(txtImpuesto2.getText());
        double impuesto3 = Double.parseDouble(txtImpuesto3.getText());
        double resultadoImp;
        
        resultadoImp = (impuesto1 + impuesto2 + impuesto3) / 3;
        
        txtPromedioImpuesto.setText(""+resultadoImp);
    }
    
    private void TotalPagar(){
        double impuesto1 = Double.parseDouble(txtImpuesto1.getText());
        double impuesto2 = Double.parseDouble(txtImpuesto2.getText());
        double impuesto3 = Double.parseDouble(txtImpuesto3.getText());
        double totalPagar;
        
        totalPagar = impuesto1 + impuesto2 + impuesto3;
        txtTotalPagar.setText(""+totalPagar);
    }
    
