    private void ValidarNombre() {
        String nombre = txtNombre.getText();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Obligatorio ingresar el nombre del cliente", "Error", JOptionPane.ERROR_MESSAGE);
        } 
    }
    
    private void ValidarMonto() {
        double monto = Double.parseDouble(txtMonto.getText());
        

        if (monto <= 0) {
            JOptionPane.showMessageDialog(this, "El monto no puede ser vacio ni menor que cero", "Error", JOptionPane.ERROR_MESSAGE);
        } 
    }
