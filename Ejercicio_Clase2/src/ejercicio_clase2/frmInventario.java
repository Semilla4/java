    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (producto != null){
            int cantidad=Integer.parseInt(JOptionPane.showInputDialog("Cantidad a agregar"));
        producto.agregarStock(cantidad);
        txtResultado.setText(producto.mostrar());
        
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String nombre=txtProducto.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());
        
        producto = new Producto(nombre, precio, cantidad);
        txtResultado.setText(producto.mostrar());
        btnAgregar.setEnabled(true);
        
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVenderActionPerformed
        if(producto != null){
            int cantidad = Integer.parseInt(txtVenta.getText());
            if (producto.vender(cantidad)){
                txtResultado.setText(producto.mostrar());
            }else {
                JOptionPane.showMessageDialog (null, "No hay stock");
            }
        }
    }//GEN-LAST:event_btnVenderActionPerformed

    private void txtVentaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtVentaKeyReleased
        String texto=txtVenta.getText();
        if(!texto.isEmpty())
        btnVender.setEnabled(true); 
        else
            btnVender.setEnabled(false);
    }//GEN-LAST:event_txtVentaKeyReleased

